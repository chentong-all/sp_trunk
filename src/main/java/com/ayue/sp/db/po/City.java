package com.ayue.sp.db.po;

public class City {
    private Integer id;

    private String name;

    private Integer rely;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getRely() {
        return rely;
    }

    public void setRely(Integer rely) {
        this.rely = rely;
    }
}
